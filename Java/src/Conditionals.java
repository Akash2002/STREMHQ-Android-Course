public class Conditionals {

    public static void main(String[] args) {

        // if something then do something

        boolean isGradeGood = false;
        int grade = 85;

        if (isGradeGood && grade > 90) {
            System.out.println("Grade is good");
        } else if (isGradeGood == false && grade < 90) {
            System.out.println("Grade is bad");
        }



    }

}
